  private categories: MenuCategory[] = (() => {
    // สร้าง Map เพื่อจัดกลุ่มตาม category
    const categoryMap = new Map<string, any[]>();

    // จัดกลุ่ม menu items ตาม category
    this.menuData.forEach((item: any) => {
      if (!categoryMap.has(item.category)) {
        categoryMap.set(item.category, []);
      }
      categoryMap.get(item.category)!.push({
        id: item.id,
        name: item.name,
        category: item.category,
        ingredients: item.ingredients,
        instructions: item.instructions,
        nutrition: item.nutrition,
      });
    });

    // แปลง Map เป็น array ของ MenuCategory
    return Array.from(categoryMap.entries()).map(([name, items]) => ({
      name,
      items,
    }));
  })();
  getCategories(): MenuCategory[] {
    return this.categories;
  }

